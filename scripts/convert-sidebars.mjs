#!/usr/bin/env node
/**
 * Reads docs.json (Mintlify) and produces sidebars-tr.ts and sidebars-en.ts
 * for the Docusaurus multi-instance docs plugin setup.
 *
 * Each Mintlify tab becomes a top-level sidebar (key on the exported object),
 * each group becomes a category, pages become doc references with the
 * locale-prefix stripped (tr/foo → foo, en/foo → foo) since each plugin
 * instance is rooted at the locale folder.
 */
import fs from 'node:fs';
import path from 'node:path';
import {fileURLToPath} from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const docsJsonPath = path.join(repoRoot, 'docs.json');

const raw = JSON.parse(fs.readFileSync(docsJsonPath, 'utf8'));

function slugify(label) {
  return label
    .toLowerCase()
    .replace(/[ığüşöç]/g, (c) => ({ı: 'i', ğ: 'g', ü: 'u', ş: 's', ö: 'o', ç: 'c'}[c] || c))
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '');
}

/**
 * Strip the locale prefix from a Mintlify page reference.
 * - Default-locale root pages (`index`, `quickstart`) were moved to tr/,
 *   so for TR plugin they resolve to `index` and `quickstart`.
 * - Pages like `tr/concepts/foo` become `concepts/foo` for the TR plugin.
 * - Pages like `en/concepts/foo` become `concepts/foo` for the EN plugin.
 * - Pages like `api-reference/foo` stay as `api-reference/foo` (en instance,
 *   we moved api-reference/ → en/api-reference/).
 */
function normalizePage(pageId, locale) {
  if (pageId.startsWith(`${locale}/`)) {
    return pageId.slice(locale.length + 1);
  }
  // For the api-reference instance, strip the `api-reference/` prefix because
  // the plugin path is rooted at api-reference/.
  if (locale === 'api-reference' && pageId.startsWith('api-reference/')) {
    return pageId.slice('api-reference/'.length);
  }
  return pageId;
}

/**
 * Resolve the on-disk directory that holds the docs for a given "locale" key.
 * For real locales (tr/en) docs live in tr/ and en/. For the api-reference
 * pseudo-locale we use the api-reference/ folder.
 */
function docsDirFor(locale) {
  if (locale === 'api-reference') return path.join(repoRoot, 'api-reference');
  return path.join(repoRoot, locale);
}

function pageFileExists(docId, locale) {
  const base = docsDirFor(locale);
  // docId is a doc reference without extension; check for .mdx then .md.
  return (
    fs.existsSync(path.join(base, `${docId}.mdx`)) ||
    fs.existsSync(path.join(base, `${docId}.md`))
  );
}

const skipped = [];

function convertPages(pages, locale) {
  const items = [];
  for (const page of pages) {
    if (typeof page === 'string') {
      const docId = normalizePage(page, locale);
      if (!pageFileExists(docId, locale)) {
        skipped.push(`[${locale}] ${page} → ${docId} (file missing)`);
        continue;
      }
      items.push(docId);
    } else if (page && typeof page === 'object' && page.group) {
      const children = convertPages(page.pages || [], locale);
      if (children.length === 0) continue; // drop empty groups
      items.push({
        type: 'category',
        label: page.group,
        collapsed: true,
        collapsible: true,
        items: children,
      });
    }
  }
  return items;
}

function convertTabs(tabs, locale) {
  const sidebars = {};
  const usedKeys = new Set();
  for (const tab of tabs) {
    let baseKey = slugify(tab.tab) || 'tab';
    let key = baseKey;
    let i = 2;
    while (usedKeys.has(key)) {
      key = `${baseKey}-${i++}`;
    }
    usedKeys.add(key);

    // Collapse single-group tabs to a flat list; otherwise create categories.
    const groups = tab.groups || [];
    let items;
    if (groups.length === 1 && groups[0].group === tab.tab) {
      items = convertPages(groups[0].pages || [], locale);
    } else {
      items = groups
        .map((g) => ({
          type: 'category',
          label: g.group,
          collapsed: true,
          collapsible: true,
          items: convertPages(g.pages || [], locale),
        }))
        .filter((cat) => cat.items.length > 0);
    }
    if (items.length === 0) continue; // skip tab with nothing left
    sidebars[key] = items;
  }
  return sidebars;
}

function emitSidebarFile(sidebars, outPath, locale) {
  const banner = `/**
 * AUTO-GENERATED by scripts/convert-sidebars.mjs from docs.json.
 * Locale: ${locale}
 * Each key on the default export corresponds to one Mintlify tab.
 */
import type {SidebarsConfig} from '@docusaurus/plugin-content-docs';

const sidebars: SidebarsConfig = `;
  const body = JSON.stringify(sidebars, null, 2);
  const file = banner + body + ';\n\nexport default sidebars;\n';
  fs.writeFileSync(outPath, file);
  console.log(`Wrote ${outPath} (${Object.keys(sidebars).length} sidebars)`);
}

/**
 * The api-reference content is global (English-only, hand-written) but is
 * referenced from BOTH the TR and EN navigation in docs.json. We move those
 * api-reference tabs out of per-locale sidebars and into a dedicated
 * sidebars-api-reference.ts that is consumed by a third docs plugin instance.
 */
function isApiReferenceTab(tab) {
  const t = (tab.tab || '').toLowerCase();
  return t === 'api reference' || t === 'api referansı' || t === 'api referansi';
}

let apiReferenceSidebars = null;

const langs = raw.navigation.languages;
for (const langBlock of langs) {
  const locale = langBlock.language;
  const tabs = (langBlock.tabs || []).filter((tab) => {
    if (isApiReferenceTab(tab)) {
      // Capture once; both locales reference the same page IDs.
      if (!apiReferenceSidebars) {
        apiReferenceSidebars = convertTabs([tab], 'api-reference');
      }
      return false;
    }
    return true;
  });
  const sidebars = convertTabs(tabs, locale);
  emitSidebarFile(sidebars, path.join(repoRoot, `sidebars-${locale}.ts`), locale);
}

if (apiReferenceSidebars) {
  emitSidebarFile(
    apiReferenceSidebars,
    path.join(repoRoot, 'sidebars-api-reference.ts'),
    'api-reference',
  );
}

if (skipped.length) {
  console.log(`\nSkipped ${skipped.length} sidebar entries with no underlying file:`);
  for (const s of skipped.slice(0, 30)) console.log(`  - ${s}`);
  if (skipped.length > 30) console.log(`  ... and ${skipped.length - 30} more`);
}

// Emit a navbar-items snippet to help wire up sidebars in docusaurus.config.ts.
const navItemsByLocale = {};
for (const langBlock of langs) {
  const locale = langBlock.language;
  navItemsByLocale[locale] = (langBlock.tabs || []).map((tab) => {
    const baseKey = slugify(tab.tab) || 'tab';
    return {sidebarId: baseKey, label: tab.tab};
  });
}
fs.writeFileSync(
  path.join(repoRoot, 'scripts', '.navbar-items.json'),
  JSON.stringify(navItemsByLocale, null, 2),
);
console.log('Wrote scripts/.navbar-items.json');
